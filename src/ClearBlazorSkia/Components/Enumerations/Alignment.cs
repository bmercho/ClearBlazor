namespace ClearBlazor
{
    /// <summary>
    /// Defines the alignment of content within its available space
    /// </summary>
    public enum Alignment
    {
        /// <summary>
        /// The content is stretched using all available space
        /// </summary>
        Stretch, 
        /// <summary>
        /// The content is aligned to the start of the available space
        /// </summary>
        Start,
        /// <summary>
        /// The content is centered in the available space
        /// </summary>
        Center,
        /// <summary>
        /// The content is aligned to the end of the available space
        /// </summary>
        End
    }
}
