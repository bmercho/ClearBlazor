namespace ClearBlazor
{
    public enum ButtonStyle
    {
        /// <summary>
        /// The button has a background fill color
        /// </summary>
        Filled, 
        /// <summary>
        /// The button is outlined
        /// </summary>
        Outlined,
        /// <summary>
        /// The button just has a label and no background and no outline
        /// </summary>
        LabelOnly,
        /// <summary>
        /// The button is the same as a Filled button but has a box shadow
        /// </summary>
        Elevated
    }
}
