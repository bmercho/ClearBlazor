namespace ClearBlazor
{
    /// <summary>
    /// The style of the input container
    /// </summary>
    public enum InputContainerStyle
    {
        /// <summary>
        /// The container has a background fill color
        /// </summary>
        Filled,
        /// <summary>
        /// The container is outlined
        /// </summary>
        Outlined,
        /// <summary>
        /// The container just has a label and no background and no outline
        /// </summary>
        LabelOnly,
        /// <summary>
        /// The container is underlined and no background and no outline
        /// </summary>
        Underlined, 
    }
}
