namespace ClearBlazor
{
    /// <summary>
    /// Defines the shape of Icon buttons and Avatars
    /// </summary>
    public enum ContainerShape
    {
        /// <summary>
        /// Circle shape
        /// </summary>
        Circle,
        /// <summary>
        /// Square shape
        /// </summary>
        Square,
        /// <summary>
        /// Has rounded corners
        /// </summary>
        Rounded,
        /// <summary>
        /// Quarter circle corners
        /// </summary>
        FullyRounded
    }
}
