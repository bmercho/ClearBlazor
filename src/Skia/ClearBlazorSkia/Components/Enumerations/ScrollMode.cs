namespace ClearBlazor
{
    /// <summary>
    /// The scroll bar mode
    /// </summary>
    public enum ScrollMode
    {
        /// <summary>
        /// No scroll bar will be shown
        /// </summary>
        Disabled, 
        /// <summary>
        /// Scroll bar will always be shown.
        /// </summary>
        Enabled, 
        /// <summary>
        /// Scroll bar will be shown when container overflowed.
        /// </summary>
        Auto
    }
}
